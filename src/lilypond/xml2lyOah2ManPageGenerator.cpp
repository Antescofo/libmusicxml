//________________________________________________________________________
xml2lyOah2ManPageGenerator::xml2lyOah2ManPageGenerator (
  const S_oahHandler handler,
  S_manPageOah&      manPageOpts,
  indentedOstream&   logOstream,
  ostream&            manPageOutputStream)
    : basicOah2ManPageGenerator (
      handler,
      manPageOpts,
      logOstream,
      manPageOutputStream)
{}

xml2lyOah2ManPageGenerator::~xml2lyOah2ManPageGenerator ()
{}

//________________________________________________________________________
void xml2lyOah2ManPageGenerator::generateManPageFromOahHandler ()
{
  fLogOutputStream <<
    ".\\\" DO NOT MODIFY THIS FILE!  It was generated by " <<
    gOahOah->fHandlerExecutableName <<
    " " <<
    currentVersionNumber () <<
    endl;

  if (fVisitedOahHandler) {
    // browse the visited oahHandler
    oahBrowser<oahHandler> browser (this);
    browser.browse (*fVisitedOahHandler);
  }
}

//________________________________________________________________________
void xml2lyOah2ManPageGenerator::visitStart (S_xml2lyVersionOahAtom& elt)
{
#ifdef TRACE_OAH
  if (gOahOah->fTraceOahVisitors) {
    fLogOutputStream <<
      "% --> Start visiting xml2lyVersionOahAtom" <<
      " \"" << elt->fetchNames () << "\"" <<
      endl;
  }
#endif

  fLogOutputStream <<
    ".HP" <<
    endl <<
    "\\fB\\-" <<
    replaceSubstringInString (elt->getShortName (), "-", "\\-") <<
    "\\fR, " <<
    "\\fB\\-" <<
    replaceSubstringInString (elt->getLongName (), "-", "\\-") <<
    "\\fR, " <<
    endl <<

    ".IP" <<
    endl <<
    elt->getDescription () <<
    endl;
}

